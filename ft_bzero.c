#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char			*s_c;
	unsigned int	i;

	s_c = (char *)s;
	i = 0;
	while (i < n)
	{
	*(s_c + i) = 0;
	i++;
	}
}
