#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nb;

	nb = n;
	if (nb < 0)
	{
		nb = -nb;
		ft_putchar_fd('-', fd);
	}	
	if (nb > 9)
	{
		ft_putnbr_fd((int)(nb / 10), fd);
		nb %= 10;
	}
	ft_putchar_fd(nb % 10 + '0', fd);
}
